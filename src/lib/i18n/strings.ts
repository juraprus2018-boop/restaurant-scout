const DICTS: Record<LocaleCode, Dict> = {
  en, nl, de, fr, es, it, pt, pl, sv, da, fi, el, cs, hu, ro, bg, sk, sl, hr, sr,
};

export function t(lang: LocaleCode, key: StringKey, vars?: Record<string, string | number>): string {
  const raw = DICTS[lang]?.[key] ?? DICTS["en"][key] ?? en[key];
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ""));
}

// Helpers for parsed opening hours / yes-no.
const DAY_CODES = ["Mo", "Tu", "We", "Th", "Fr", "Sa", "Su", "PH"] as const;
type DayCode = typeof DAY_CODES[number];

export function weekdayLabel(lang: LocaleCode, code: string): string {
  return DAY_CODES.includes(code as DayCode)
    ? t(lang, `wd.${code}` as StringKey)
    : code;
}

export function yesNoLabel(lang: LocaleCode, v?: string | null): string | null {
  if (!v) return null;
  switch (v) {
    case "yes": return t(lang, "yn.yes");
    case "no": return t(lang, "yn.no");
    case "limited": return t(lang, "yn.limited");
    case "designated": return t(lang, "yn.designated");
    case "only": return t(lang, "yn.only");
    case "wlan": return "Wi-Fi";
    case "terminal": return "ATM";
    default: return v;
  }
}

// Parse opening hours like "Mo-Th 09:00-03:00; Fr-Su 09:00-04:00" into rows
// with localized day names.
export function parseOpeningHoursI18n(raw: string, lang: LocaleCode): Array<{ days: string; hours: string }> {
  const to = t(lang, "wd.to");
  return raw
    .split(";")
    .map((s) => s.trim())
    .filter(Boolean)
    .map((part) => {
      const m = part.match(/^([A-Za-z,\-]+)\s+(.+)$/);
      if (!m) return { days: "", hours: part };
      const days = m[1].split(",").map((seg) => {
        const range = seg.split("-");
        if (range.length === 2) {
          return `${weekdayLabel(lang, range[0])} ${to} ${weekdayLabel(lang, range[1])}`;
        }
        return weekdayLabel(lang, seg);
      }).join(", ");
      return { days, hours: m[2] };
    });
}
